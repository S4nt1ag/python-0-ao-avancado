# Tipo de dado bool (Boolean)
# Ao questionar algo em um programa, só existe duas respostas possiveis: sim (true) ou não (false).
# Existem vários operadores para "questionar".
# Dentre eles 0 ==, que é um operador lógico que questiona se um valor é igual a outro

print(10 == 10) # Sim => True 
print(10 == 11) # Não => False
print(type(10 == 10)) # Tipo bool