# Tipos int e float


# int -> Número inteiro
# O tipo int representa qualquer número positivo ou negativo.
# Int sem sinal é considerado positivo

print(11) # int
print(-11) # int
print(0) # int


# float -> Número com ponto flutuante
# O tipo flutuante representa qualquer número positivo ou negativo com ponto flutuante.
# Float sem sinal é considerado positivo

print(1.1) # Float
print(-1.1) # Float
print(0.0) # Float


# A função type mostra o tipo que o Python inferiu o valor

print(type('Otavio'))
print(type(11))
print(type(1.1))